let x = 0;
let y = 0;
let cntX = 0;
let cntY = 0;


function setup(){
	createCanvas(windowWidth, windowHeight);
	frameRate(8);
    noLoop();
}

function draw(){
	background(255);

    fill(200);
    while(cntX <= 18){
        rect(x, 0, 50, 50);
        x += 50;
        cntX += 1;
    }
    while(cntY <= 18){
        rect(0, y, 50, 50);
        y += 50;
        cntY += 1;
    }
    
    noStroke();

    // 赤色
	fill(167, 0, 0);
	rect(250, 100, 250, 50); //帽子
	rect(200, 150, 450, 50);

	rect(300, 450, 50, 100); //オーバーオール
	rect(450, 500, 50, 50);
	rect(300, 550, 200, 50);
	rect(250, 600, 300, 150);
	rect(200, 700, 150, 100);
	rect(450, 700, 150, 100);

    // 黄色
    fill(243, 191, 63);
    rect(350, 200, 200, 50); //顔
    rect(300, 250, 350, 50);
    rect(350, 300, 350, 50);
    rect(250, 350, 200, 50);
    rect(250, 400, 350, 50);
    rect(200, 250, 50, 100);

    rect(100, 600, 100, 150); //手
    rect(200, 650, 50, 50);
    rect(600, 600, 100, 150);
    rect(550, 650, 50, 50);

    rect(300, 600, 50, 50); //ボタン
    rect(450, 600, 50, 50);
    

    // 緑色
    fill(0, 63, 23);
    rect(200, 200, 150, 50);　//顔
    rect(150, 250, 50, 50);
    rect(250, 250, 50, 50);
    rect(150, 300, 50, 50);
    rect(250, 300, 100, 50);
    rect(150, 350, 100, 50);
    rect(450, 200, 50, 100);
    rect(500, 300, 50, 50);
    rect(450, 350, 200, 50);

    rect(200, 450, 100, 50); //胴体
    rect(350, 450, 150, 50);
    rect(150, 500, 150, 50);
    rect(350, 500, 100, 50);
    rect(500, 500, 150, 50);
    rect(100, 550, 200, 50);
    rect(500, 550, 200, 50);
    rect(200, 600, 50, 50);
    rect(550, 600, 50, 50);

    rect(150, 800, 150, 50); //靴
    rect(500, 800, 150, 50);
    rect(100, 850, 200, 50);
    rect(500, 850, 200, 50);

    
}